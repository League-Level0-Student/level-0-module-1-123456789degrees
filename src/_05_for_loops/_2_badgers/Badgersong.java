package _05_for_loops._2_badgers;

public class Badgersong {
	public static void main(String[] args) {
		for (int j = 0; j < 2; j++) {
			for (int i = 0; i < 12; i++) {
				System.out.print("Badger ");
			}
			for (int i = 0; i < 2; i++) {
				System.out.print("Mushroom ");
			}
			System.out.println("\n");
		}
		System.out.println("\nA Snake!!!");
	}
}
